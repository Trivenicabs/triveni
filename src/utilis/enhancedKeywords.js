// Export function to combine all keywords for a specific page
export function getAllKeywordsForPage(cityName, destination = null) {
  const keywords = [];
  
  // Add city-specific keywords
  if (citySpecificKeywords[cityName]) {
    keywords.push(...citySpecificKeywords[cityName]);
  }
  
  // Add route-specific keywords if destination provided
  if (destination) {
    const routeKey = `${cityName}-${destination}`;
    if (routeSpecificKeywords[routeKey]) {
      keywords.push(...routeSpecificKeywords[routeKey]);
    }
    
    // Also check reverse route
    const reverseRouteKey = `${destination}-${cityName}`;
    if (routeSpecificKeywords[reverseRouteKey]) {
      keywords.push(...routeSpecificKeywords[reverseRouteKey]);
    }
    
    // Add destination city keywords
    if (citySpecificKeywords[destination]) {
      keywords.push(...citySpecificKeywords[destination]);
    }
  }
  
  // Add all universal keywords
  keywords.push(...semanticKeywords);
  keywords.push(...vehicleTypeKeywords);
  keywords.push(...localAreaKeywords);
  
  // Remove duplicates
  return [...new Set(keywords)];
}

// Export function to get keyword count
export function getKeywordCount() {
  const allKeywords = new Set();
  
  // Count city-specific
  Object.values(citySpecificKeywords).forEach(keywords => {
    keywords.forEach(kw => allKeywords.add(kw));
  });
  
  // Count route-specific
  Object.values(routeSpecificKeywords).forEach(keywords => {
    keywords.forEach(kw => allKeywords.add(kw));
  });
  
  // Add universal keywords
  semanticKeywords.forEach(kw => allKeywords.add(kw));
  vehicleTypeKeywords.forEach(kw => allKeywords.add(kw));
  localAreaKeywords.forEach(kw => allKeywords.add(kw));
  
  return allKeywords.size;
}

export default {
  citySpecificKeywords,
  routeSpecificKeywords,
  semanticKeywords,
  vehicleTypeKeywords,
  localAreaKeywords,
  getAllKeywordsForPage,
  getKeywordCount
};